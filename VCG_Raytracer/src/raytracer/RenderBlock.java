package raytracer;

public class RenderBlock {

    public int startX;
    public  int endX;
    public  int startY;
    public int endY;

    public RenderBlock(int startX, int endX, int startY, int endY){
        this.startX = startX;
        this.endX = endX;
        this.startY = startY;
        this.endY = endY;
    }
}
